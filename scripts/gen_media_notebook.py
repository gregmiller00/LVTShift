    '\n'
    '    # Condominium\n'
    '    if any(x in leg for x in ["CONDO", "CONDOMINIUM"]):\n'
    '        return "Condominium"\n'
    '\n'
    '    # Townhome / Rowhouse\n'
    '    if any(x in leg for x in ["ROW HSE", "ROWHOUSE", "TOWNHOUSE", "TOWNHOME",\n'
    '                               "TOWN HSE", "END ROW", "INT ROW"]):\n'
    '        return "Townhome / Rowhouse"\n'
    '\n'
    '    # Mixed use\n'
    '    if any(x in leg for x in ["MIX", "MIXED", "STORE & APT", "STORES & APT",\n'
