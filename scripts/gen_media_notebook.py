# ── 2b: iasWorld scraping ────────────────────────────────────────────────────
cells.append(code_cell(
    'ASSESS_CACHE_PATH = DATA_DIR / "iasworld_assessments.csv"\n'
    '\n'
    '\n'
    'def parse_iasworld_page(html):\n'
    '    """Parse iasWorld profileall_pub page for total assessed value + property type."""\n'
    '    from bs4 import BeautifulSoup\n'
    '\n'
    '    soup = BeautifulSoup(html, "html.parser")\n'
    '\n'
    '    # Property type from the Parcel summary table\n'
    '    property_type = None\n'
    '    parcel_table = soup.find("table", {"id": "Parcel"})\n'
    '    if parcel_table:\n'
    '        for row in parcel_table.find_all("tr"):\n'
    '            cells = row.find_all("td")\n'
    '            if len(cells) >= 2 and "Property Type" in cells[0].get_text():\n'
    '                property_type = cells[1].get_text(strip=True)\n'
    '                break\n'
    '\n'
    '    # Total assessed value from "Original Current Year Assessment" table\n'
    '    assessed_value = None\n'
    '    asmt_table = soup.find("table", {"id": "Original Current Year Assessment"})\n'
    '    if asmt_table:\n'
    '        for row in asmt_table.find_all("tr"):\n'
    '            cells = row.find_all("td", class_="DataletData")\n'
    '            if len(cells) >= 2:\n'
    '                val = re.sub(r"[$,\\s]", "", cells[1].get_text(strip=True))\n'
    '                if val.lstrip("-").isdigit():\n'
    '                    assessed_value = int(val)\n'
    '                    break\n'
    '\n'
    '    return assessed_value, property_type\n'
    '\n'
    '\n'
    'def scrape_parcel(parid_int):\n'
    '    """Fetch one iasWorld parcel page and return parsed data."""\n'
    '    url = (\n'
    '        "http://delcorealestate.co.delaware.pa.us/pt/datalets/datalet.aspx"\n'
    '        f"?mode=profileall_pub&usesearch=no&jur=023&taxyr=2026&pin={parid_int}"\n'
    '    )\n'
    '    try:\n'
    '        r = requests.get(url, timeout=20)\n'
    '        r.raise_for_status()\n'
    '        av, pt = parse_iasworld_page(r.text)\n'
    '        return {"parid": parid_int, "assessed_value": av, "property_type_raw": pt}\n'
    '    except Exception:\n'
    '        return {"parid": parid_int, "assessed_value": None, "property_type_raw": None}\n'
    '\n'
    '\n'
    'if ASSESS_CACHE_PATH.exists() and not data_scrape:\n'
    '    df_assess = pd.read_csv(ASSESS_CACHE_PATH)\n'
    '    print(f"Loaded {len(df_assess):,} assessment records from cache")\n'
    'else:\n'
    '    parids = [\n'
    '        int(v) for v in gdf_geo["PARID"].dropna().unique()\n'
    '        if not pd.isna(v)\n'
    '    ]\n'
    '    print(f"Scraping iasWorld for {len(parids):,} parcels (20 concurrent workers)...")\n'
    '    results = []\n'
    '    with concurrent.futures.ThreadPoolExecutor(max_workers=20) as executor:\n'
    '        futures = {executor.submit(scrape_parcel, pid): pid for pid in parids}\n'
    '        for i, future in enumerate(concurrent.futures.as_completed(futures)):\n'
    '            results.append(future.result())\n'
    '            if (i + 1) % 200 == 0:\n'
    '                print(f"  {i + 1}/{len(parids)} done")\n'
    '    df_assess = pd.DataFrame(results)\n'
    '    df_assess.to_csv(ASSESS_CACHE_PATH, index=False)\n'
    '    print(f"Scraped and cached {len(df_assess):,} records")\n'
    '\n'
    'print(f"Null assessed values: {df_assess[\\"assessed_value\\"].isna().sum()}")\n'
    'print(df_assess["property_type_raw"].value_counts().head(10))\n'
))

